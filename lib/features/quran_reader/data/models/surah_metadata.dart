/// Surah metadata information.
///
/// Contains basic information about a surah that doesn't require
/// loading the full Quran data.
class SurahMetadata {
  const SurahMetadata({
    required this.number,
    required this.nameArabic,
    required this.nameEnglish,
    required this.nameTranslation,
    required this.revelationType,
    required this.ayahCount,
  });

  final int number;
  final String nameArabic;
  final String nameEnglish;
  final String nameTranslation;
  final String revelationType;
  final int ayahCount;

  /// Whether this surah was revealed in Mecca.
  bool get isMeccan => revelationType == 'Meccan';

  /// Whether this surah was revealed in Medina.
  bool get isMedinan => revelationType == 'Medinan';
}

/// Repository of all 114 surah metadata.
///
/// This is static data that doesn't require network or asset loading.
class SurahMetadataRepository {
  const SurahMetadataRepository._();

  /// Get metadata for a specific surah (1-114).
  static SurahMetadata getSurah(int surahNumber) {
    if (surahNumber < 1 || surahNumber > 114) {
      throw RangeError.range(surahNumber, 1, 114, 'surahNumber');
    }
    return allSurahs[surahNumber - 1];
  }

  /// Get all surah metadata.
  static List<SurahMetadata> get allSurahs => _surahList;

  /// Search surahs by name (Arabic or English) or number.
  static List<SurahMetadata> search(String query) {
    final String normalizedQuery = query.toLowerCase().trim();
    if (normalizedQuery.isEmpty) return [];

    return _surahList.where((surah) {
      return surah.nameArabic.contains(query) ||
          surah.nameEnglish.toLowerCase().contains(normalizedQuery) ||
          surah.number.toString() == normalizedQuery;
    }).toList();
  }
}

